const COURSE_URL = "https://thecodeman.net/pragmatic-dotnet-code-rules";

// Analyzer package versions. Pinned to known-good releases; the UI tells users to
// check nuget.org for the latest. Bump here when you verify newer versions.
const PKG = {
  stylecop: { id: "StyleCop.Analyzers", version: "1.2.0-beta.556" },
  sonar: { id: "SonarAnalyzer.CSharp", version: "9.32.0.97167" },
  roslynator: { id: "Roslynator.Analyzers", version: "4.12.9" },
};

const DEFAULTS = {
  dotnetVersion: "net10.0", // net6.0 | net7.0 | net8.0 | net9.0 | net10.0
  projectType: "solution", // solution | webapi | library | console
  strictness: "balanced", // relaxed | balanced | strict
  indentStyle: "spaces", // spaces | tabs
  indentSize: 4, // 2 | 4
  fileScopedNamespaces: true,
  underscorePrivateFields: true,
  varPreference: "apparent", // always | apparent | none
  stylecop: true,
  sonar: true,
  roslynator: false,
  treatWarningsAsErrors: null, // null => derived from strictness
};

const LANG_VERSION = {
  "net6.0": "10.0",
  "net7.0": "11.0",
  "net8.0": "12.0",
  "net9.0": "13.0",
  "net10.0": "14.0",
};

// strictness -> severities used throughout the config
function severities(strictness) {
  switch (strictness) {
    case "relaxed":
      return { style: "suggestion", quality: "suggestion", naming: "suggestion", analysisMode: "Default" };
    case "strict":
      return { style: "error", quality: "warning", naming: "warning", analysisMode: "All" };
    case "balanced":
    default:
      return { style: "warning", quality: "warning", naming: "warning", analysisMode: "Recommended" };
  }
}

function sanitize(input = {}) {
  const o = { ...DEFAULTS, ...input };
  if (!LANG_VERSION[o.dotnetVersion]) o.dotnetVersion = DEFAULTS.dotnetVersion;
  if (!["relaxed", "balanced", "strict"].includes(o.strictness)) o.strictness = "balanced";
  if (!["spaces", "tabs"].includes(o.indentStyle)) o.indentStyle = "spaces";
  o.indentSize = [2, 4].includes(Number(o.indentSize)) ? Number(o.indentSize) : 4;
  if (!["always", "apparent", "none"].includes(o.varPreference)) o.varPreference = "apparent";
  if (!["solution", "webapi", "library", "console"].includes(o.projectType)) o.projectType = "solution";
  o.fileScopedNamespaces = !!o.fileScopedNamespaces;
  o.underscorePrivateFields = !!o.underscorePrivateFields;
  o.stylecop = !!o.stylecop;
  o.sonar = !!o.sonar;
  o.roslynator = !!o.roslynator;
  if (o.treatWarningsAsErrors === null || o.treatWarningsAsErrors === undefined) {
    o.treatWarningsAsErrors = o.strictness === "strict";
  } else {
    o.treatWarningsAsErrors = !!o.treatWarningsAsErrors;
  }
  return o;
}

function varRules(pref) {
  if (pref === "apparent") {
    return [
      "csharp_style_var_for_built_in_types = false:suggestion",
      "csharp_style_var_when_type_is_apparent = true:suggestion",
      "csharp_style_var_elsewhere = false:suggestion",
    ];
  }
  const want = pref === "always" ? "true" : "false";
  return [
    `csharp_style_var_for_built_in_types = ${want}:suggestion`,
    `csharp_style_var_when_type_is_apparent = ${want}:suggestion`,
    `csharp_style_var_elsewhere = ${want}:suggestion`,
  ];
}

