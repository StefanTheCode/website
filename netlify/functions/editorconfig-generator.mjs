// EditorConfig Generator (FREE tool) - answer a few questions and get a
// production-ready .editorconfig + Directory.Build.props for your .NET solution,
// built from TheCodeMan's "Pragmatic .NET Code Rules" methodology.
//
// The config files are generated DETERMINISTICALLY from vetted templates, so the
// analyzer rule IDs and syntax are always valid. The LLM is used ONLY to add a
// short, plain-English "why these choices" explanation and - when the user adds
// free-text requirements - a clearly-labeled block of AI-suggested extra rules.
// If the LLM is unavailable the tool still returns the deterministic base config
// plus fallback explanation, so it never breaks.
//
// POST /api/editorconfig-generator
//   { options: {...}, additionalRequirements?: string, email?: string }
// Returns:
//   { editorconfig, props, fileName, propsFileName, notes:[{title,text}],
//     summary, extraApplied, courseUrl, remaining }

