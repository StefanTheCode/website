function buildProps(o) {
  const sev = severities(o.strictness);
  const lang = LANG_VERSION[o.dotnetVersion];
  const pkgs = [];
  if (o.stylecop) pkgs.push(PKG.stylecop);
  if (o.sonar) pkgs.push(PKG.sonar);
  if (o.roslynator) pkgs.push(PKG.roslynator);

  const pkgBlock = pkgs.length
    ? `

  <!-- Analyzers applied to every project. Check nuget.org for newer versions. -->
  <ItemGroup>
${pkgs
  .map(
    (pk) =>
      `    <PackageReference Include="${pk.id}" Version="${pk.version}">
      <PrivateAssets>all</PrivateAssets>
      <IncludeAssets>runtime; build; native; contentfiles; analyzers</IncludeAssets>
    </PackageReference>`,
  )
  .join("\n")}
  </ItemGroup>`
    : "";

  return `<!-- Directory.Build.props - generated by TheCodeMan EditorConfig Generator -->
<!-- https://thecodeman.net/tools/editorconfig-generator -->
<!-- Place at the root of your solution. Every project inherits these settings. -->
<Project>

  <PropertyGroup>
    <TargetFramework Condition="'$(TargetFramework)' == ''">${o.dotnetVersion}</TargetFramework>
    <LangVersion>${lang}</LangVersion>
    <Nullable>enable</Nullable>
    <ImplicitUsings>enable</ImplicitUsings>
  </PropertyGroup>

  <PropertyGroup>
    <!-- Enforce .editorconfig code-style rules during build, not just in the IDE. -->
    <EnforceCodeStyleInBuild>true</EnforceCodeStyleInBuild>
    <EnableNETAnalyzers>true</EnableNETAnalyzers>
    <AnalysisLevel>latest</AnalysisLevel>
    <AnalysisMode>${sev.analysisMode}</AnalysisMode>
    <TreatWarningsAsErrors>${o.treatWarningsAsErrors ? "true" : "false"}</TreatWarningsAsErrors>
    <GenerateDocumentationFile>true</GenerateDocumentationFile>
    <!-- CS1591: don't require XML doc on every public member; analyzers cover the rest. -->
    <NoWarn>$(NoWarn);CS1591</NoWarn>
  </PropertyGroup>${pkgBlock}

</Project>
`;
}

// Deterministic explanation - always available, even with no LLM key.
function fallbackNotes(o) {
  const sev = severities(o.strictness);
  const notes = [
    {
      title: `Strictness: ${o.strictness}`,
      text:
        o.strictness === "strict"
          ? "Code-style violations are errors and TreatWarningsAsErrors is on, so the build fails on any dirty code. Best for new or well-maintained solutions."
          : o.strictness === "relaxed"
            ? "Most rules are suggestions, so nothing blocks your build yet. Ideal for a large legacy codebase you want to clean up gradually before tightening."
            : "Style and quality rules are warnings, surfaced in the build and CI without blocking day-one. The pragmatic default for most teams.",
    },
    {
      title: `Target: ${o.dotnetVersion} (C# ${LANG_VERSION[o.dotnetVersion]})`,
      text: `Directory.Build.props pins LangVersion to ${LANG_VERSION[o.dotnetVersion]}, enables Nullable and ImplicitUsings, and turns on EnforceCodeStyleInBuild so your .editorconfig rules run at build time, not just in Visual Studio.`,
    },
    {
      title: o.fileScopedNamespaces ? "File-scoped namespaces" : "Block-scoped namespaces",
      text: o.fileScopedNamespaces
        ? "Enforces the modern `namespace Foo;` form, removing a level of indentation across every file."
        : "Keeps the classic braced `namespace Foo { }` form for compatibility with older code.",
    },
  ];
  const analyzers = [o.stylecop && "StyleCop", o.sonar && "SonarAnalyzer", o.roslynator && "Roslynator"].filter(Boolean);
  if (analyzers.length) {
    notes.push({
      title: `Analyzers: ${analyzers.join(", ")}`,
      text: `Referenced once in Directory.Build.props so every project gets them automatically. Noisy rules that fight modern C# (e.g. SA1101, SA1200) are pre-disabled; the high-value ones run at ${sev.quality}.`,
    });
  }
  notes.push({
    title: "Naming",
    text: `Interfaces require an I prefix, types and members are PascalCase${
      o.underscorePrivateFields ? ", and private fields use _camelCase" : ""
    }, all enforced at ${sev.naming} level.`,
  });
  return notes;
}

// Keep only lines that look like real .editorconfig directives, so an AI
// suggestion can never inject prose or break the file. Allows comments,
// [section] headers, and key = value pairs with editorconfig-style keys.
function sanitizeExtraConfig(lines) {
  if (!Array.isArray(lines)) return [];
  const keyVal = /^[a-z0-9_.:]+\s*=\s*.+$/i;
  const section = /^\[.+\]$/;
  const comment = /^#/;
  return lines
    .map((l) => String(l).trim())
    .filter((l) => l && (comment.test(l) || section.test(l) || keyVal.test(l)))
    .slice(0, 30);
}

export default async (req, context) => {
  if (req.method === "OPTIONS") return preflight(req);
  if (req.method !== "POST") return json(req, 405, { error: "Method not allowed" });

  const body = await readJson(req);
  const email = typeof body.email === "string" ? body.email.trim() : "";
  const o = sanitize(body.options || body);
  const additionalRequirements = String(
    body.additionalRequirements || (body.options && body.options.additionalRequirements) || "",
  )
    .slice(0, 1000)
    .trim();

  const ip = clientIp(req, context);
  const limit = email ? 40 : 12;
  const rl = await checkRateLimit(`editorconfig:${email || ip}`, limit);
  if (!rl.ok) {
    return json(req, 429, {
      error: "Daily limit reached. Drop your email for more, or come back tomorrow.",
    });
  }

  let editorconfig = buildEditorConfig(o);
  const props = buildProps(o);

  // AI explanation layer (optional). Falls back to deterministic notes if the LLM
  // is unavailable. When the user adds free-text requirements, the AI also proposes
  // EXTRA .editorconfig lines, which are sanitized to real directives and appended
  // in a clearly-labeled "review before committing" block. The deterministic base
  // above is never modified.
  let notes = fallbackNotes(o);
  let summary = "";
  let extraApplied = false;
  try {
    const wantsExtra = additionalRequirements.length > 0;
    const analyzerList =
      [o.stylecop && "StyleCop", o.sonar && "Sonar", o.roslynator && "Roslynator"].filter(Boolean).join(",") || "none";

    const extraInstruction = wantsExtra
      ? "The developer wrote extra requirements in plain language. Translate ONLY the parts that map to real .editorconfig directives into extraConfig lines (key = value, or a leading # comment). If something cannot be expressed in .editorconfig (e.g. CI setup, Git hooks), do NOT invent a rule; instead mention it in a note. Never duplicate a rule already implied by the chosen options."
      : "There are no extra requirements; return an empty extraConfig array.";

    const systemLines = [
      "You are explaining a generated .NET .editorconfig + Directory.Build.props to a developer, on TheCodeMan.net (by Microsoft MVP Stefan Djokic).",
      "Be concrete, practical and .NET-specific. Do NOT invent rules; only use real, documented .editorconfig keys and analyzer rule IDs (e.g. dotnet_diagnostic.CA1822.severity, csharp_style_*, dotnet_style_*).",
      extraInstruction,
      "Respond with STRICT JSON only, no markdown:",
      '{"summary":"<one tight sentence>","notes":[{"title":"<short>","text":"<1-2 sentences>"}],"extraConfig":["<editorconfig line>"]}',
      "Max 5 notes. Max 20 extraConfig lines.",
    ];
    const system = systemLines.join("\n");

    const userLines = [
      `Chosen options: ${JSON.stringify(o)}.`,
      `Key facts: strictness=${o.strictness}, target=${o.dotnetVersion} (C# ${LANG_VERSION[o.dotnetVersion]}), file-scoped namespaces=${o.fileScopedNamespaces}, analyzers=${analyzerList}, TreatWarningsAsErrors=${o.treatWarningsAsErrors}, EnforceCodeStyleInBuild=true.`,
    ];
    if (wantsExtra) {
      userLines.push(`Extra requirements (plain language): ${additionalRequirements}`);
    }
    const userMsg = userLines.join("\n\n");

    const raw = await askLLM({
      system,
      tier: "fast",
      maxTokens: 800,
      temperature: 0.3,
      messages: [{ role: "user", content: userMsg }],
    });
    const parsed = JSON.parse(raw.replace(/^```json\s*|\s*```$/g, "").trim());

    if (Array.isArray(parsed.notes) && parsed.notes.length) {
      notes = parsed.notes
        .filter((n) => n && n.title && n.text)
        .slice(0, 5)
        .map((n) => ({ title: String(n.title), text: String(n.text) }));
    }
    if (typeof parsed.summary === "string") summary = parsed.summary;

    if (wantsExtra) {
      const extra = sanitizeExtraConfig(parsed.extraConfig);
      if (extra.length) {
        const header = [
          "",
          "# ============================================================",
          "# Additional rules - AI-suggested from your requirements.",
          "# Review these before committing; the rules above are the vetted base.",
          "# ============================================================",
        ].join("\n");
        editorconfig += header + "\n" + extra.join("\n") + "\n";
        extraApplied = true;
      }
    }
  } catch {
    /* keep deterministic fallback notes */
  }

  // If the user asked for custom rules but we couldn't reach the AI, be honest.
  if (additionalRequirements && !extraApplied) {
    notes = [
      {
        title: "Custom requirements",
        text: "The base config above is complete and valid. We couldn't generate AI-tailored rules for your custom requirements right now; try again in a moment for suggestions.",
      },
      ...notes,
    ];
  }

  return json(req, 200, {
    editorconfig,
    props,
    fileName: ".editorconfig",
    propsFileName: "Directory.Build.props",
    notes,
    summary,
    extraApplied,
    courseUrl: COURSE_URL,
    remaining: rl.remaining,
  });
};
